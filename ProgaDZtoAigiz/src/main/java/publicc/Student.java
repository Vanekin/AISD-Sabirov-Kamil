package publicc;

public class Student {
    public String name;
    public String middleName;
    public String lastName;
    public Integer age;
    public String group;


    public Student() {}

    public Student(String name, String middleName, String lastName, Integer age, String group) {
        this.name = name;
        this.middleName = middleName;
        this.lastName = lastName;
        this.age = age;
        this.group = group;
    }

}
